<?php

namespace App\Admin\Controllers\User;

use App\Http\Constant\Constant;
use App\Models\User;

use Encore\Admin\Form;
use Encore\Admin\Grid;
use Encore\Admin\Facades\Admin;
use Encore\Admin\Layout\Content;
use App\Http\Controllers\Controller;
use Encore\Admin\Controllers\ModelForm;

class UserAdminController extends Controller
{
    use ModelForm;

    /**
     * Index interface.
     *
     * @return Content
     */
    public function index()
    {
        return Admin::content(function (Content $content) {

            $content->header('header');
            $content->description('description');

            $content->body($this->grid());
        });
    }

    /**
     * Edit interface.
     *
     * @param $id
     * @return Content
     */
    public function edit($id)
    {
        return Admin::content(function (Content $content) use ($id) {

            $content->header('header');
            $content->description('description');

            $content->body($this->form()->edit($id));
        });
    }

    /**
     * Create interface.
     *
     * @return Content
     */
    public function create()
    {
        return Admin::content(function (Content $content) {

            $content->header('header');
            $content->description('description');

            $content->body($this->form());
        });
    }

    /**
     * Make a grid builder.
     *
     * @return Grid
     */
    protected function grid()
    {
        return Admin::grid(User::class, function (Grid $grid) {

            $grid->id('ID')->sortable();
            $grid->nickname('昵称');
            $grid->avatar('头像')->image(null,50,50);
            $grid->sex('性别')->display(function ($sex){
                switch ($sex){
                    case Constant::SEX_UNKNOWN:
                        return buildLabel('danger',Constant::SEX_MAP[$sex]);
                        break;
                    case Constant::SEX_BOY:
                        return buildLabel('info',Constant::SEX_MAP[$sex]);
                        break;
                    case Constant::SEX_GIRL:
                        return buildLabel('success',Constant::SEX_MAP[$sex]);
                        break;
                }
            });
            $grid->created_at('注册时间');
        });
    }

    /**
     * Make a form builder.
     *
     * @return Form
     */
    protected function form()
    {
        return Admin::form(User::class, function (Form $form) {

            $form->display('id', 'ID');

            $form->display('created_at', 'Created At');
            $form->display('updated_at', 'Updated At');
        });
    }
}
